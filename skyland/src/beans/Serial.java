package beans;

public class Serial {
	private int serial;
	private int goodsNum;
	private boolean isSold;

	public void setSerial(int serial){this.serial=serial;}
	public int getSerial(){return this.serial;}

	public void setGoodsNum(int goodsNum){this.goodsNum=goodsNum;}
	public int getGoodsNum(){return this.goodsNum;}
	
	public void setIsSold(boolean isSold){this.isSold=isSold;}
	public boolean getIsSold(){return this.isSold;}
}
